    <h2>2. ASSIGNMENT OF RECEIVABLES</h2>
    <ol>
        <li>Subject to the terms and conditions set forth in this Agreement, the Vendor hereby assigns to the Financier and the Financier hereby purchases from the Vendor, free from any encumbrance, the Receivables due on the Due Date from the Anchor for a purchase consideration of Rs. (Rupees (Purchase Consideration)) to be paid to the Vendor’s Account as per the timeline set out in Annexure 1. The date of transfer of the Receivables shall be the date on which the payment is made to the Vendor.</li>
        <li>Each of the Parties agree that upon payment of the Purchase Consideration by the Financier to the Vendor, all of the Vendor’s right, title, and interest in the Receivables (including all rights to enforce payment of the Receivables against the Anchor) shall vest in the Financier and the Financier shall have the sole right to enforce its right, title and interest in the Receivables against the Anchor.</li>
        <li>Vendor agrees and undertakes to promptly issue notice of the assignment of Receivables to the Anchor as prescribed under the relevant provisions of the Transfer of Property Act, 1882.</li>
        <li>The Financier acknowledges and agrees that OAL’s role in the assignment of Receivables shall be limited to the facilitation of such assignment.</li>
